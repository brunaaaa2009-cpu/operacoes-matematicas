let numUm;
let numDois;
let resultado;
//let declara uma váriavel 
//Number serve para o caracter ser interpretado com um número
// prompt abre uma caixa de texto para o usuário digitar
numUm = Number(prompt("Digite o primeiro numero"));
numDois = Number(prompt("Digite o segundo numero"));
resultado = numUm - numDois
alert(`A subtração dos Dois números é ${resultado.toFixed(2)}`);
