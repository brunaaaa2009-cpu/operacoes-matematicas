let numUm;
let numDois;
let result;

// let declara uma variável.
// Number serve para o caracter ser interpretado como um número.
// prompt abre uma caixa de texto para o usuário digitar.
numUm = Number(prompt("digite o primeiro número"));
numDois = Number(prompt("digite o segundo número"));
result = numUm * numDois;
alert(`O resultado da multiplicação é ${result}`);
// alert exibe uma mensagem na tela.

let resultado;

resultado = numUm - numDois
alert(`A subtração dos Dois números é ${resultado.toFixed(2)}`);
//alert exibe uma mensagem na tela  

let divResul;
//let dita o nome de uma variável.

divResul = numUm / numDois;
//Number prompt faz a máquina ver os caracteres não como um texto e sim com número.

alert(`O resultado dessa divisão é ${divResul}`);
//alert exibe uma frase na tela.
